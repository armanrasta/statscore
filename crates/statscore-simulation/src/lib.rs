//! # `statscore-simulation`
//!
//! Simulation methods: bootstrap confidence intervals, permutation tests,
//! Monte Carlo integration, and simulation-based power analysis.
//!
//! ## Planned modules
//! - `bootstrap` — nonparametric, parametric, percentile and BCa intervals
//! - `permutation` — permutation tests
//! - `monte_carlo` — generic Monte Carlo integration
//! - `quasi_monte_carlo` — Sobol sequences
//! - `power_simulation` — simulation-based power analysis
//!
//! ## Dependencies
//! - [`statscore-common`] — types and errors
//! - [`statscore-distributions`] — parametric resampling
//!
